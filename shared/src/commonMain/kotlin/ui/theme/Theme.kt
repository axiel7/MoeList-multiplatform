fun ColorScheme.toAmoled() = this.copy(
    background = md_theme_black_background,
    surface = md_theme_black_background,
    surfaceVariant = surfaceVariant.copy(alpha = 0.4f).compositeOver(md_theme_black_background)
)

@Composable
fun MoeListTheme(
    darkTheme: Boolean = isSystemInDarkTheme(),
    //dynamicColor: Boolean = true,
    amoledColors: Boolean = false,
    content: @Composable () -> Unit
) {
    val windowInsetsController = rememberWindowInsetsController()
    DisposableEffect(windowInsetsController, darkTheme) {
        windowInsetsController?.setStatusBarContentColor(dark = darkTheme)
        windowInsetsController?.setNavigationBarsContentColor(dark = darkTheme)
        onDispose { }
    }

    MaterialTheme(
        colorScheme = getColorScheme(darkTheme, amoledColors),
        typography = Typography,
        content = content
    )
}